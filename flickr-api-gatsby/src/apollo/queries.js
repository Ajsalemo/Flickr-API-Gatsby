import gql from "graphql-tag"

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------- //
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------- //

export const RANDOM_IMAGE_SEARCH = gql`
    query randomImageSearch {
        randomImage @rest(type: "Random Image", path: "photos/random?featured=true&count=20") {
            description
            id
            location @type(name: "location") {
                __typename
                name
                city
                country
            }
            urls @type(name: "urls"){
                regular
                full
            }
        }
    }
`
